def usd_to_eur(usd="10")
  usd * 0.86 
end

def eur_to_usd(eur)
  eur / 0.86 
end

def usd_to_jpy(usd)
  usd * 111.08
end

def jpy_to_usd(jpy)
  jpy / 111.08
end

def usd_to_gbp(usd)
  usd * 0.76
end

def gbp_to_usd(gbp)
  gbp / 0.76
end

def usd_to_aud(usd)
  usd * 1.35
end

def aud_to_usd(aud)
  aud / 1.35
end



